/**
 * Get level configuration for specific industry and level
 */
export function getLevelConfig(industry: IndustryType, level: CompanyLevel, subcategory?: 'AI' | 'Software' | 'Hardware'): LevelConfig | undefined {
  const configs = COMPANY_LEVELS[industry];
  if (!configs || configs.length === 0) return undefined;
  
  if (industry === 'Technology' && subcategory) {
    return configs.find(c => c.level === level && c.subcategory === subcategory);
  }
  
  return configs.find(c => c.level === level);
}

/**
 * Get next level config
 */
export function getNextLevelConfig(industry: IndustryType, currentLevel: CompanyLevel, subcategory?: 'AI' | 'Software' | 'Hardware'): LevelConfig | undefined {
  if (currentLevel >= 5) return undefined;
  return getLevelConfig(industry, (currentLevel + 1) as CompanyLevel, subcategory);
}
